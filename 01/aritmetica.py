print('1. Calcula el área de un rectángulo con base 15 y altura 8.')
print('\nPará calcular el área del rectángulo, primero plasmamos los datos.\nDatos:')

base = 15
altura = 8
print(' Base =', base, '\n Altura =', altura)

area_rectangulo = base * altura
print( '\nYá con ello usamos la formula para calcular.\n Base.Altura = Área')

print('\nEl área del rectángulo es: ' + str(area_rectangulo))
